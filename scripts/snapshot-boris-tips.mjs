#!/usr/bin/env node
// Snapshot the local boris skill markdown into a build-time JSON the dashboard
// can serve at /tips/[n]. Run on demand (e.g. when boris ships a new Part).

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { homedir } from "node:os";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = join(HERE, "..");
const SKILL_PATH = join(homedir(), ".claude", "skills", "boris", "SKILL.md");
const INDEX_PATH = join(ROOT, "app", "data", "boris-tip-index.json");
const OUT_PATH = join(ROOT, "app", "data", "boris-tips-content.json");

if (!existsSync(SKILL_PATH)) {
  console.error(`Boris skill not found at ${SKILL_PATH}`);
  console.error("Install it via: curl -L -o ~/.claude/skills/boris/SKILL.md https://howborisusesclaudecode.com/api/install");
  process.exit(1);
}

const md = readFileSync(SKILL_PATH, "utf8");
const index = JSON.parse(readFileSync(INDEX_PATH, "utf8"));

// Strip the file's frontmatter and the early scaffolding so we only parse the
// body where "## N. Title" sections live.
const body = md.replace(/^---[\s\S]*?\n---\n/, "");

// Match "## 7. Hooks" headers and capture content up to the next "## " or EOF.
// JS regex has no \Z; we anchor on the next "## N." or end-of-string with $(?![\s\S]).
const sectionRe = /^## (\d+)\.\s+(.+)$\n([\s\S]*?)(?=^## \d+\.\s+|$(?![\s\S]))/gm;

const tips = {};
for (const m of body.matchAll(sectionRe)) {
  const n = parseInt(m[1], 10);
  const title = m[2].trim();
  let content = m[3].trim();
  // Strip trailing horizontal-rule separators before the next section
  content = content.replace(/\n*-{3,}\s*$/m, "").trim();

  const idx = index.tips[String(n)];
  tips[String(n)] = {
    n,
    title,
    contentMd: content,
    volume: idx?.volume ?? null,
    tab: idx?.tab ?? null,
    label: idx?.label ?? null,
    topic: idx?.topic ?? title,
  };
}

const output = {
  $schema: "Generated by scripts/snapshot-boris-tips.mjs from ~/.claude/skills/boris/SKILL.md.",
  capturedAt: new Date().toISOString(),
  source: "howborisusesclaudecode.com/api/install (local snapshot)",
  site: index.site,
  tips,
};

writeFileSync(OUT_PATH, JSON.stringify(output, null, 2) + "\n");
console.log(`Wrote ${Object.keys(tips).length} tips to ${OUT_PATH}`);
